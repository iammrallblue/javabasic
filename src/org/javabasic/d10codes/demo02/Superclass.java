package org.javabasic.d10codes.demo02;

/*
  A Superclass.
*/
public class Superclass {
  // instance variable
  int num = 10;

  // instance method
  public void methodA() {
    System.out.println("method in Superclass.");
  }

  public void superMethod() {
    System.out.println("specific method in Superclass.");
  }

  public void printNumber() {
    System.out.println(num);
  }

  public void method() {
    System.out.println("method in Superclass.");
  }
}
